# 更通用的场景特征词（适合所有行业的日常表达）
SCENE_KEYWORDS: list[str] = [
    "上班", "下班", "公司", "团队", "同事", "领导",
    "家", "学校", "教室", "实验室",
    "外出", "出差", "会议", "展会", "客户",
    "市场", "路边", "街", "店铺",
]


def extract_domains(fragments: list[dict]) -> list[str]:
    """
    从碎片列表中提取用户所属的行业/生活领域。

    Args:
        fragments: [{type, content}, ...]

    Returns:
        ["餐饮", "销售/商务", ...] 按匹配度降序，最多3个
    """
    from collections import Counter

    domain_scores: Counter = Counter()
    all_text = " ".join(f.get("content", "") for f in fragments)

    for domain, keywords in DOMAIN_KEYWORDS.items():
        score = 0
        for kw in keywords:
            if kw in all_text:
                score += 1
        if score > 0:
            domain_scores[domain] = score

    top_domains = [d for d, _ in domain_scores.most_common(3)]
    return top_domains


def extract_scene_context(fragments: list[dict]) -> str:
    """
    从碎片中提取一个具体的场景描述（用于 market_hint / next_action）。
    返回简洁的场景短语，比如："你现在的餐饮工作"、"你上班的地方"、"你的小店"

    Returns:
        str: 场景描述文本，如果找不到返回空字符串
    """
    all_text = " ".join(f.get("content", "") for f in fragments)

    # 先检查匹配到的具体领域
    domains = extract_domains(fragments)
    if domains:
        domain = domains[0]
        # 根据领域生成场景描述
        scene_map = {
            "餐饮": "你现在的餐饮工作",
            "零售/电商": "你的店铺",
            "IT/互联网": "你现在的工作",
            "教育/培训": "你的教学场景",
            "销售/商务": "你的销售日常",
            "物流/运输": "你的物流工作",
            "医疗/健康": "你的医疗工作",
            "创意/设计": "你的创作日常",
            "行政/人事": "你的办公室日常",
            "财务/金融": "你的财务工作",
            "制造/工程": "你的工厂/工地",
            "自媒体/内容": "你的内容创作",
            "社群/社区": "你的社群运营",
        }
        if domain in scene_map:
            return scene_map[domain]

    # 检查通用场景词
    for kw in SCENE_KEYWORDS:
        if kw in all_text:
            if kw in ("公司", "上班", "下班", "同事"):
                return "你现在的职场"
            elif kw in ("家",):
                return "你的日常生活"
            elif kw in ("学校", "教室"):
                return "你的学习场景"
            elif kw in ("店铺", "市场"):
                return "你的生意"
            break

    return ""


def extract_scene_hints(fragments: list[dict]) -> list[str]:
    """
    提取具体的场景片段文本（用于方向展示）。
    返回匹配到场景关键词的碎片内容片段。

    Args:
        fragments: [{type, content}, ...]

    Returns:
        ["内容片段1", "内容片段2", ...]
    """
    hints = []
    for f in fragments:
        c = f.get("content", "")
        for kw in ["上班", "下班", "店", "家", "学校", "社区", "小区", "公司", "工厂", "市场"]:
            if kw in c:
                hints.append(c[:20])
                break
    return hints
