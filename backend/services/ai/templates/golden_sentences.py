def get_golden_sentence(frag_summary: str, user_id: str = "", category: str = "") -> str:
    """
    获取一句金句，同一用户多次调用尽量不重复。

    Args:
        frag_summary: 碎片摘要（用于填充模板中的 {summary}）
        user_id: 用户标识（用于去重轮换）
        category: 情绪类型，空则随机
    """
    import hashlib
    import random

    # 展开所有句子
    all_sentences: list[tuple[str, str]] = []  # [(category, template)]
    for cat, sentences in GOLDEN_SENTENCES.items():
        for s in sentences:
            all_sentences.append((cat, s))

    # 用 user_id + frag_summary 做确定性但可轮换的选择
    seed = int(hashlib.md5((user_id + frag_summary).encode()).hexdigest()[:4], 16)
    rng = random.Random(seed)

    if category and category in GOLDEN_SENTENCES:
        pool = [(cat, s) for cat, s in all_sentences if cat == category]
    else:
        pool = all_sentences

    chosen_cat, template = rng.choice(pool)
    return template.format(summary=frag_summary)
