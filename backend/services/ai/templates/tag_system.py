def classify_tags(text: str) -> list[tuple[str, float]]:
    """
    用关键词匹配将文本分类到 8 维能力标签。

    匹配策略：
    1. 精确关键词匹配，较长关键词权重更高
    2. 同一个 tag 多个关键词命中时分数累加
    3. 返回 top-3，分数降序

    Args:
        text: 碎片内容

    Returns:
        [(标签名, 匹配分), ...] 按分数降序
    """
    scores: dict[str, float] = {}
    for tag, info in TAG_DEFINITIONS.items():
        score = 0.0
        matched_kws = []
        for kw in info["keywords"]:
            if kw in text:
                kw_score = 1.0 + (len(kw) - 2) * 0.15
                score += kw_score
                matched_kws.append(kw)
        if score > 0:
            scores[tag] = score

    sorted_tags = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    return sorted_tags[:3]


def get_top_tags(text: str, limit: int = 2) -> list[str]:
    """获取文本的 Top-N 能力标签"""
    tagged = classify_tags(text)
    return [tag for tag, _ in tagged[:limit]]


def build_tag_profile(fragments: list[dict]) -> dict:
    """
    从碎片列表中构建用户的能力标签画像。

    Returns:
        {
            "top_tags": ["内容创作", "资源整合"],
            "tag_counts": {"内容创作": 3, "资源整合": 2, ...},
            "signature_tag": "内容创作×资源整合的跨界者",
        }
    """
    from collections import Counter
    tag_counts: Counter = Counter()

    for f in fragments:
        content = f.get("content", "")
        tags = get_top_tags(content, limit=2)
        for tag in tags:
            tag_counts[tag] += 1

    top = tag_counts.most_common(3)
    top_tags = [t for t, _ in top]

    signature = "×".join(top_tags[:2]) + "的跨界者" if len(top_tags) >= 2 else top_tags[0] + "专长者" if top_tags else "探索者"

    return {
        "top_tags": top_tags,
        "tag_counts": dict(tag_counts),
        "signature_tag": signature,
    }
