def get_insight(total_fragments: int, frag_summary: str, combo_type: str = "跨维度组合") -> str:
    """
    获取一句洞察，按碎片组合类型选择。

    Args:
        total_fragments: 碎片总数
        frag_summary: 碎片摘要
        combo_type: 组合类型（技能型组合/经历型组合/性格型组合/跨维度组合/目标驱动型/探索型组合）
    """
    import hashlib
    import random

    templates = INSIGHT_TEMPLATES.get(combo_type, INSIGHT_TEMPLATES["跨维度组合"])
    seed = int(hashlib.md5(frag_summary.encode()).hexdigest()[:4], 16)
    rng = random.Random(seed)
    template = rng.choice(templates)
    return template.format(total=total_fragments, summary=frag_summary)
