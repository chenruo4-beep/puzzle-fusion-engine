def get_spark_for_pair(type_a: str, type_b: str, content_a: str, content_b: str) -> dict:
    """
    根据两个碎片类型获取火花模板。

    Args:
        type_a: 第一个碎片类型
        type_b: 第二个碎片类型
        content_a: 第一个碎片内容
        content_b: 第二个碎片内容

    Returns:
        {"title": "...", "spark": "...", "action": "..."}
    """
    import random

    # 截短内容用于填充模板
    c0 = content_a[:10] if len(content_a) > 10 else content_a
    c1 = content_b[:10] if len(content_b) > 10 else content_b

    key = (type_a, type_b)
    variants = SPARK_COMBO_TEMPLATES.get(key, [
        {
            "title": f"「{c0}」×「{c1}」",
            "spark": f"你会{c0}，又有{c1}相关——这两个维度交叉的地方，往往藏着你没发现的优势。试着把它们放进同一个场景里想一想。",
            "action": "花5分钟写下：这两个能力组合在一起，能解决一个什么问题？",
        }
    ])

    chosen = random.choice(variants)
    return {
        "title": chosen["title"].format(c0=c0, c1=c1),
        "spark": chosen["spark"].format(c0=c0, c1=c1),
        "action": chosen["action"].format(c0=c0, c1=c1),
    }
